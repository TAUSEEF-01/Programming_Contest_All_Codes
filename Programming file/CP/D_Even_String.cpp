const ll M = 998244353;

ll powerMod(ll x, ll y, ll p)
{
    ll res = 1;
    x = x % p;

    if (x == 0)
        return 0;

    while (y > 0)
    {
        if (y & 1)
            res = (res * x) % p;
        y = y >> 1;
        x = (x * x) % p;
    }

    return res;
}

ll inverse(ll a)
{
    return powerMod(a, M - 2, M);
}

vector<ll> vec(500007), ivec(500007);
void facto()
{
    vec[0] = 1;
    ivec[0] = 1;
    for (ll i = 1; i < 500005; i++)
    {
        vec[i] = (vec[i - 1] * i) % M;
        ivec[i] = inverse(vec[i]);
    }
}

void solve()
{
    vector<ll> a(27, 0);

    ll n = 0;
    for (int i = 1; i < 27; i++)
    {
        cin >> a[i];
        n += a[i];
    }

    vector<vector<ll>> dp(27, vector<ll>(n + 1, 0));
    dp[0][0] = 1;

    for (int i = 1; i < 27; i++)
    {
        for (int j = 0; j <= n / 2; j++)
        {
            dp[i][j] = dp[i - 1][j];
            if (a[i] > 0 && j - a[i] >= 0)
            {
                dp[i][j] = (dp[i - 1][j - a[i]] + dp[i][j]) % M;
            }
        }
    }

    ll ans = (dp[26][n / 2] * vec[((n + 1) / 2)]) % M;
    ans = (ans * vec[(n / 2)]) % M;

    for (int i = 1; i < 27; i++)
    {
        ans = (ans * ivec[a[i]]) % M;
    }

    cout << ans << endl;
}

