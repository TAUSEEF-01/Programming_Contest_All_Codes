void solve()
{
    ll n;
    cin >> n;

    vl a(n + 1), pref(n + 1, 0);
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
        pref[i] = pref[i - 1] + a[i];
    }

    ll q;
    cin >> q;

    while (q--)
    {
        ll l, u;
        cin >> l >> u;

        ll lo = l, hi = n, r = l, mx = -inf;
        while (lo <= hi)
        {
            ll mid = (hi + lo) / 2;

            ll tasks = pref[mid] - pref[l - 1];
            ll points = 0;
            if (tasks < u)
            {
                ll left = u - tasks;
                points = u * (u + 1) / 2 - (left * (left + 1) / 2);
            }
            else
            {
                ll left = tasks - u - 1;
                points = (u * (u + 1) / 2) - (left * (left + 1) / 2);
            }
            
            
            ll tasks2 = pref[mid + 1] - pref[l - 1];
            ll points2 = 0;
            if (tasks2 < u)
            {
                ll left = u - tasks2;
                points2 = u * (u + 1) / 2 - (left * (left + 1) / 2);
            }
            else
            {
                ll left = tasks2 - u - 1;
                points2 = (u * (u + 1) / 2) - (left * (left + 1) / 2);
            }
            

            if (mx < points)
            {
                mx = points;
                r = mid;
            }
            else if (mx == points)
            {
                r = min(r, mid);
            }

            
            if (points2 <= points)
            {
                hi = mid - 1;
            }
            else
            {
                lo = mid + 1;
            }
        }

        cout << r << ' ';
    }
    cout << endl;
}

