int sumOfDigitsFrom1ToNUtility(int num, int arr[])
{
    if (num < 10)
    {
        return (num * (num + 1) / 2);
    }

    int power = (int)(log10(num));
    int p = (int)(ceil(pow(10, power)));
    int msDigit = num / p;
    return (msDigit * arr[power] + (msDigit * (msDigit - 1) / 2) * p +
            msDigit * (1 + num % p) + sumOfDigitsFrom1ToNUtility(num % p, arr));
}

int sumOfDigitsFrom1ToN(int num)
{
    int power = (int)(log10(num));
    int arr[num + 1];
    arr[0] = 0;
    arr[1] = 45;
    for (int j = 2; j <= power; j++)
    {
        arr[j] = arr[j - 1] * 10 + 45 * (int)(ceil(pow(10, j - 1)));
    }

    return sumOfDigitsFrom1ToNUtility(num, arr);
}


// another approach --> precalculating the values
int sum_digits_1_to_n[200005];
void sum_of_digits_from_1_to_n()
{
    // pre calculating the sums
    int sum = 0;
    for (int i = 0; i < 200005; i++)
    {
        int num = i, digit_sum = 0;
        while (num)
        {
            digit_sum += num % 10;
            num /= 10;
        }
        sum += digit_sum;
        sum_digits_1_to_n[i] = sum;
    }
}

