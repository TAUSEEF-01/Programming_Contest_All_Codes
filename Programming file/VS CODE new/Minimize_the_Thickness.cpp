#include<bits/stdc++.h>

using namespace std;

int main()
{
    //Let's define thickness of split as the length of the longest segment
    //Find the minimum thickness among all possible splits
    int t,n,a;
    cin>>t;
    for(int i=0; i<t; i++)
    {
        cin>> n;
        for(int j=0; j<n; j++)
        {
            cin>> a;
        }
        cout<< a;
    }
    return 0;
}