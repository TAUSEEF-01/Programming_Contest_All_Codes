void solve()
{
    ll n;
    cin >> n;

    vl a(n), ac(n);
    ll sum = 0;
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }

    ac = a;

    asort(a);
    vl pre(n - 1);
    for (int i = 0; i < n - 1; i++)
    {
        sum += a[i];
        pre[i] = sum;
    }

    int ans = n - 1;
    map<ll, int> mp;
    for (int i = n - 1; i > 0; i--)
    {
        if (a[i] > pre[i - 1])
        {
            mp[a[i]] = ans;
            ans = i;
            ans--;
        }
        else
        {
            mp[a[i]] = ans;
        }
    }
    mp[a[0]] = ans;

    for (int i = 0; i < n; i++)
    {
        cout << mp[ac[i]] << ' ';
    }

    cout << endl;
}

