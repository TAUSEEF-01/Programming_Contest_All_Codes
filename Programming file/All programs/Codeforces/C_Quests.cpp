void solve()
{
    ll n, k;
    cin >> n >> k;

    vl a(n), b(n);

    for (auto &x : a)
        cin >> x;
    for (auto &x : b)
        cin >> x;

    ll val_max = b[0], suma = a[0], sumb = a[0], pos = 0, bmax=b[0];
    ll MIN = min(n, k), MAX=max(n, k);
    for (int i = 1; i < MIN; i++)
    {
        suma += a[i];

        // val_max = max(val_max, b[i-1]);
        // val_max = max(val_max, b[pos]);
        bmax=max(bmax, b[i]);
        sumb+=val_max;

        // if (a[i] >= val_max && i == pos)
        if(suma >= sumb)
        {
            sumb = suma;
            // pos = i;
            val_max=bmax;
            continue;
        }

        if (i < MIN - 1)
        {
            // if (suma + b[i]*(MAX-i) >= sumb + val_max*(MAX-i))
            if (suma + b[i] >= sumb + val_max)
            {
                sumb = suma;
                val_max=bmax;
                // pos=i;
            }
        //     // else
        //     // {
        //     //     sumb += val_max;
        //     // }
        }
        // else
        // {
            // sumb += val_max;
            // if(pos==i-1)
            // val_max=bmax;
        // }
    }

    if(suma>=sumb) sumb=suma;
    ll sum; //max(suma, sumb);

    if(suma+(k-n)*bmax>=sumb+(k-n)*val_max)
    {
        // sum=suma+(k-n)*bmax;
        sum=suma;
        val_max=bmax;
    }
    else
    {
        // sum=sumb+(k-n)*val_max;
        sum=sumb;
    }
    while (k > MIN)
    {
        sum += val_max;
        k--;
    }

    cout << sum << endl;
}

