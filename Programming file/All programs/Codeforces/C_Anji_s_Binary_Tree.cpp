const int dfs_limit = 3e5 + 10;

string a;
int l[dfs_limit], r[dfs_limit];
int ans = 1e9;

void dfs(ll u, int distance)
{
    int childs = l[u] + r[u];

    if (!childs)
        ans = min(ans, distance);

    if (l[u])
    {
        dfs(l[u], distance + (a[u] != 'L'));
    }
    if (r[u])
    {
        dfs(r[u], distance + (a[u] != 'R'));
    }
}

void solve() // using dfs
{
    int n;
    cin >> n;

    cin >> a;
    a = '#' + a;

    for (int i = 1; i <= n; i++)
    {
        cin >> l[i] >> r[i];
    }

    ans = 1e9;

    dfs(1, 0);

    cout << ans << endl;
}

